package com.mateus.ahundredOfJavaBasic.day006;

/** Operadores Matematicos em Java */
public class OperadoresMatematicos {
	
	public static void main(String[] args) {
		
		int A = 2;
		int B = 2;
		
		/** Somando */
		int resultado1 = A + B;
		
		/** Subtraindo */
		int resultado4 = A - B;
		
		/** Dividindo */
		int resultado2 = A / B;
		
		/** Multiplicando */
		int resultado3 = A * B;
		
		/** Modulo ou Resto */
		int resultado5 = A % B;

	}
}
